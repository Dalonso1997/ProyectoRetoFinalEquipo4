    class Material {
        String nombre;
        int stock;
        boolean prestable;

        public Material(String nombre, int stock, boolean prestable) {
            this.nombre = nombre;
            this.stock = stock;
            this.prestable = prestable;
        }

        @Override
        public String toString() {
            return nombre + " (Stock: " + stock + ")";
        }
    }
    
