    //metodo para meter las ubicaciones en el desplegable
    private void cargarUbicaciones() {
        //hacemos una instancia del dao de ubicacion
        UbicacionDAO ubiDAO = new UbicacionDAO();
        //metemos todas las ubicaciones de la base de datos en una lista
        List<Ubicacion> lista = ubiDAO.listarTodos();

        //vaciamos el desplegable para asegurarnos de que no haya datos repetidos
        jComboBox1.removeAllItems();

        //recorremos la lista de ubicaciones una a una
        for (Ubicacion u : lista) {
            //si es la actual no la metemos
            if (!u.toString().equals(ubicacionActualTexto)) {
                //si es una ubicacion nueva, la añadimos al desplegable
                jComboBox1.addItem(u.toString());
            }
        }
    }

