    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String seleccion = (String) jComboBox1.getSelectedItem();
        if (seleccion != null) {
            UbicacionDAO ubiDAO = new UbicacionDAO();
            int idNuevaUbi = -1;
            for (Ubicacion u : ubiDAO.listarTodos()) {
                if (u.toString().equals(seleccion)) {
                    idNuevaUbi = u.getId_ubicacion();
                    break;
                }
            }
            if (idNuevaUbi != -1) {
                MaterialDAO matDAO = new MaterialDAO();
                if (matDAO.cambioUbicacionMaterial(idNuevaUbi, idMaterial)) {
                    JOptionPane.showMessageDialog(this, "Ubicacion actualizada.");
                    this.dispose();
                }
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

